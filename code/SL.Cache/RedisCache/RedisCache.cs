        #region 初始化

        public RedisCache(int dbNum = 0) : this(dbNum, null)
        {
        }

        /// <summary>Constructor.</summary>
        /// <remarks>ygz, 2017/03/16.</remarks>
        /// <param name="dbNum"> The database number.</param>
        /// <param name="rwHost">The read and write host.</param>
        public RedisCache(int dbNum, string rwHost)
        {
            _dbNum = dbNum;
            _redis = string.IsNullOrEmpty(rwHost) ? RedisConn.Redis : RedisConn.GetConnectionMultiplexer(rwHost);
        }

        #endregion

        #region 辅助方法

        private string AddSysCustomKey(string oldKey)
        {
            var prefixKey = CustomKey ?? RedisConn.SysCustomKey;
            return prefixKey + oldKey;
        }

        private string ConvertJson<T>(T value)
        {
            string json = value is string ? value.ToString() : JsonConvert.SerializeObject(value);
            return json;
        }

        private T DbHandle<T>(Func<IDatabase, T> func)
        {
            IDatabase db = _redis.GetDatabase(_dbNum);
            return func(db);
        }

        private T ConvertObj<T>(RedisValue value)
        {
            return string.IsNullOrEmpty(value) ? default(T) : JsonConvert.DeserializeObject<T>(value);
        }

        private List<T> ConvetList<T>(RedisValue[] values)
        {
            List<T> list = new List<T>();
            foreach (var item in values)
            {
                var model = string.IsNullOrEmpty(item) ? default(T) : JsonConvert.DeserializeObject<T>(item);
                list.Add(model);
            }
            return list;
        }

        private List<RedisKey> ConvertRedisKeys(List<string> redisKeys)
        {
            return redisKeys.Select(redisKey => (RedisKey) redisKey).ToList();
        }

